def aylana_hisobla(num):
    """Aylana diametri, perimetri va yuzini hisoblovchi funksiya"""
    print(f"Radiusi - {num}\n"
          f"Diametri - {num*2}\n"
          f"Uzunligi - {2*num*3.14}\n"
          f"Yuzi - {3.14*(num**2)}")

aylana_hisobla(36)
        










